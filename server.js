var express = require('express');
var path = require('path');
var favicon = require('serve-favicon');
var logger = require('morgan');
var cookieParser = require('cookie-parser');
var bodyParser = require('body-parser');
var fs = require('fs');

// declare routes requirments
var routes = {};
var files = fs.readdirSync(path.join(__dirname, 'routes'));

files.forEach(function(file) {
  routes[file.split('.')[0]] = require('./routes/' + file);
});

var app = express();

// view engine setup
// app.set('views', path.join(__dirname, 'views'));
// app.set('view engine', 'jade');

// uncomment after placing your favicon in /public
app.use(favicon(__dirname + '/app/favicon.ico'));
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({
  extended: false
}));
app.use(cookieParser());

// dev env 
app.use(express.static(path.join(__dirname, 'app')));

// product env
// app.use(express.static(path.join(__dirname, 'dist')));

// ajax routes

app.get('/rank', routes.getRank.getHandle);

app.get('/gamedata/:id', routes.gamedata.getHandle)
  .post('/gamedata', routes.gamedata.postHandle);


// catch 404 and forward to error handler
app.use(function(req, res, next) {
  var err = new Error('Not Found');
  err.status = 404;
  next(err);
});

// development error handler
// will print stacktrace
if (app.get('env') === 'development') {
  app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.send({
      message: err.message,
      error: err
    });
  });
}

// production error handler
// no stacktraces leaked to user
app.use(function(err, req, res, next) {
  res.status(err.status || 500);
  res.send({
    message: err.message,
    error: {}
  });
});

module.exports = app;
